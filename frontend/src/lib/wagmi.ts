// Check if contract is properly configured (not the placeholder address)
export const isContractConfigured = () => {
  const configured = ANON_ID_CONTRACT_ADDRESS && 
         ANON_ID_CONTRACT_ADDRESS !== '0x1234567890123456789012345678901234567890' &&
         ANON_ID_CONTRACT_ADDRESS.length === 42 &&
         ANON_ID_CONTRACT_ADDRESS.startsWith('0x')
  
  console.log('Contract configuration check:', {
    address: ANON_ID_CONTRACT_ADDRESS,
    configured
  })
  
  return configured
}
